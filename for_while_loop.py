# print numbers from 1 to 100 using for and while loops :
#while loop
i = 1
while(i<=100):
    print(i)
    i+=1
print("we print 1 to 100 using while looop ------")


#for loop 
for i in range(1,101):
    print(i)
print("We print 1 to 100 using for loop ------")

# print numbers from 100 to 1 using for and while loops :
i = 100
while(i>=1):
    print(i)
    i-=1
print("we print 100 to 1 using while looop ------")


#for loop 
for i in range(100,0,-1):
    print(i)
print("We print 100 to 1 using for loop ------")